// 2. Difference between SQL and MongoDB?
// SQL (Relational)	MongoDB (NoSQL)
// Tables and rows	Collections and documents
// Schema required	Schema-less / dynamic
// JOINs supported	No joins (but $lookup)
// ACID transactions	Available (since v4.0)


